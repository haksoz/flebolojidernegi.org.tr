                                        <p class="Metin"><b><span>Mahremiyete
    saygı<o:p></o:p></span></b></p>
                                        <p class="Metin"><b><span>MADDE 6- </span></b><span>(1) Tanıtım ve bilgilendirme faaliyetlerinde, hasta
    hakları ihlâlleri ile bunlara bağlı ortaya çıkan sorunların önlenmesi ve
