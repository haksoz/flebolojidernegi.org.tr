    rıza, Ek-1’de yer alan Görsel İçerik Kaydetme ve İşleme Onam Formu
    kullanılarak yazılı veya elektronik ortamda alınabilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>c) Hasta, paylaşılacak görsel içeriği önceden görme
    hakkına sahiptir. Görsel paylaşım izni, hastanın kendisinin, küçük veya
    mahcur ise veli veya vasisinin isteği üzerine herhangi bir usul ve şarta
    bağlanmaksızın her zaman geri çekilebilir. Hastanın geri çekme talebini
    sağlık tesisi veya ilgili sağlık meslek mensubu derhal işleme almak ve sonuçlandırmakla
    yükümlüdür.<o:p></o:p></span></p>
                                        <p class="Metin"><span>ç) Görüntü paylaşım izni vermeyen hastalara, tanı
    ve tedavi uygulamalarında ve alınacak bedellerde herhangi bir değişiklik
    olmayacağı bilgi ve güvencesinin verilmesi zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>d) Görsel içeriklerinin kullanılabilmesi için
    alınan izin karşılığında hastaya herhangi bir ödeme ya da indirim
    yapılamaz, hediye verilemez.<o:p></o:p></span></p>
                                        <p class="Metin"><span>e) Tanıtım ve bilgilendirmede kullanılacak video ve
    fotoğraflara ait ortamların, kullanılan tekniklerin gerçeğine aykırı
    olmaması, yanıltıcı makyaj olmaksızın görüntülenmesi zorunludur. Görsel
    içerikler üzerinde sonradan teknolojik değişiklik veya düzeltme
    uygulanamaz.<o:p></o:p></span></p>
                                        <p class="Metin"><span>f) Tıbbi işlem öncesi ve sonrası görsellerin aynı
    ortam ve teknik şartlarda görüntülenmesi ve görsel içeriklerde işlem tarihi
    ve görselin görüntülendiği tarihlerin belirtilmesi zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>g) Paylaşılan görsellerin ilgili sağlık meslek
    mensubuna ve gerçek hastaya ait veya alıntı olup olmadığının, alıntı ise
    kaynağının belirtilmesi zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>ğ) Başka mecralarda yapılmış olsa dahi yazılı veya
    görsel basın ve yayın organları, sosyal medya platformları, internet
    siteleri gibi mecralarda hasta veya yakınlarının sağlık hizmetine yönelik teşekkür
    veya memnuniyet ifadeleri üzerinden reklam mahiyetinde görsel paylaşım
    yapılamaz. Görsel içeriklere ait paylaşımların yorum, beğeni ve yeniden
    paylaşım gibi kullanıcı etkileşimlerine kapatılması zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>h) Tıbbi müdahale ve girişim veya ameliyat
    esnasındaki hasta görüntüsü paylaşılamaz.<o:p></o:p></span></p>
                                        <p class="Metin"><span>ı) Genel ahlak kurallarına aykırılık teşkil edecek
    şekilde vücudun mahrem bölgelerine ait görsel içerikler paylaşılamaz.
    Yapılan işlemin detayları ve işlem yapılan vücut bölgesi ile ilgisi olmayan
    hiçbir görüntü ve ifade görsele <span class="GramE">dahil</span> edilemez.<o:p></o:p></span></p>
                                        <p class="Metin"><span>i) Görsel içerikli paylaşımlar, sağlık tesisi veya
    ilgili sağlık meslek mensubunun kendisi tarafından yapılmak zorundadır. Bu
    paylaşımların başkaları tarafından yapılmış olması ilgili sağlık tesisi
    veya sağlık meslek mensubunun sorumluluğunu ortadan kaldırmaz.<o:p></o:p></span></p>
                                        <p class="Metin"><span>j) Paylaşılan görsel içerikler; yazılı, görsel
    basın, sosyal medya platformları, internet siteleri gibi mecralarda <span class="GramE">sponsorlu</span> olarak ya da ücret verilerek yayınlatılamaz.<o:p></o:p></span></p>
                                        <p class="Metin"><span>k) Yurt içi tanıtım ve bilgilendirme faaliyetlerinde
    kullanılan görsel içeriklerde uyarı ve bilgilendirme amaçlı ve kolay
    okunabilir görünürlükteki "Her cerrahi veya girişimsel işlemde
    sonuçlar kişiden kişiye değişiklik gösterebilir. İşlem öncesinde
    hekiminizden detaylı görüş almanız önerilir." ifadesine yer verilmesi
    zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><b><span>Uluslararası
    sağlık turizmi tanıtım ve bilgilendirme faaliyetleri<o:p></o:p></span></b></p>
                                        <p class="Metin"><b><span>MADDE 8- </span></b><span>(1) <span class="GramE">26/4/2025</span> tarihli ve
    32882 sayılı Resmî Gazete’de yayımlanan Uluslararası Sağlık Turizmi ve
    Turistin Sağlığı Hakkında Yönetmelik uyarınca Bakanlıkça verilen yetki
    belgesi kapsamında uluslararası sağlık hizmeti sunan sağlık tesisleri ile
    aracı kuruluşlar tarafından aşağıdaki ilke ve esaslara uymak kaydıyla
    tanıtım ve bilgilendirme faaliyetlerinde bulunulabilir:<o:p></o:p></span></p>
                                        <p class="Metin"><span>a) Uluslararası sağlık turizmi alanında tanıtım ve
    bilgilendirme faaliyetlerinin yurt dışına yönelik ayrı bir sosyal medya
    platformu veya internet sitesi üzerinden yürütülmesi ve bu platformlarda
    sağlık turizmi kapsamında hizmet sunulduğunun açıkça beyan edilmesi
    kaydıyla, Türkçe hariç diğer resmî dillerde <span class="GramE">sponsorlu</span>
    tanıtım ve bilgilendirme faaliyeti yapılabilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>b) Sağlık turizmi yetki belgesinin, uluslararası
    sağlık turizmi hizmeti sunan sağlık tesisine veya aracı kuruluşa ait
    internet sitesinde veya sosyal medya platformunda yayınlanması zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>c) Türkiye’de yaşayan kişiler nezdinde talep
    oluşturmaya yönelik tanıtım ve bilgilendirme faaliyeti gösterilmesi
    yasaktır. Sosyal medya platformlarında hedef kitle olarak yurt içi
    seçilemez ve otomatik hedef kitle tanımlamalarının devre dışı bırakılması
    zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>ç) Uluslararası sağlık turizmi hizmeti sunan sağlık
    tesisi; yurt dışına yönelik oluşturulan internet sitesi veya sosyal medya
    platformunda, hasta mahremiyeti, genel ahlak kuralları ve hasta haklarına
    riayet etmek ve hastanın açık rızasının alındığını belgelendirmek şartıyla;
    tedavi gören hastaların hikâyelerine, hasta yorumlarına veya teşekkür
    mahiyetindeki ifadelere yer verebilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>d) Uluslararası sağlık turizmi hizmeti sunan sağlık
    tesisi, yurt dışına yönelik oluşturulan internet sitesi veya sosyal medya
    platformunda hedef kitlenin yurt dışı olarak seçilmesi, genel ahlaka aykırı
    olmaması ve otomatik hedef kitle tanımlamalarının kapatılması kaydıyla
    Türkçe hariç tüm resmi dillerde <span class="GramE">sponsorlu</span> olarak
    görsel içerik yayınlayabilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>e) Uluslararası sağlık turizmi hizmeti sunan sağlık
    tesisi; sağlık turisti ve turistin sağlığı hastasına yönelik yürütülen
    tanıtım ve bilgilendirme faaliyetlerinde “<span class="SpellE">HealthTürkiye</span>”
    logosunu tüm mecralarda kullanmakla yükümlüdür.<o:p></o:p></span></p>
                                        <p class="Metin"><span>f) Uluslararası sağlık turizmi hizmeti sunan sağlık
    tesisine ait internet sitesinde yer alan sağlık tesisi ismi ve <span class="SpellE">ünvanı</span> ile URL adresinin, sağlık tesisinin Bakanlıkça
    verilmiş ruhsatında yazan <span class="SpellE">ünvan</span> ve sahiplik adı
    ile uyumlu olması zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>g) Aracı kuruluş; ilgili mevzuatında tanımlanan
    aracı kuruluş hizmetleriyle sınırlı olmak, sağlık hizmet sunumuna yönelik
    faaliyetleri çağrıştırmamak veya sağlık tesisi izlenimi vermemek kaydıyla <span class="GramE">sponsorlu</span> tanıtım ve bilgilendirme yapabilir ve görsel
    içerik paylaşabilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>ğ) Aracı kuruluş, protokol imzaladığı sağlık
    hizmetinin ilgili sağlık tesisi tarafından verildiğini ve kendisinin sağlık
    tesisi olmadığını belirtmek kaydıyla aracılık ettiği sağlık hizmetini,
    sağlık tesisi isimlerini ve bu tesise ait resmî internet sitesini bilgi
    amaçlı olarak paylaşabilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>h) Aracı kuruluşa ait internet siteleri URL
    adreslerinin, ticari <span class="SpellE">ünvan</span>, işletme adı veya
    bunların kısaltmalarını taşıması zorunludur.<o:p></o:p></span></p>
                                        <p class="Metin"><span>ı) Marka tescilini URL adresinde kullanmak isteyen
    uluslararası sağlık turizmi hizmeti sunan sağlık tesisi ve aracı kuruluş,
    tescil belgelerini Bakanlığa bildirmekle yükümlüdür.<o:p></o:p></span></p>
