    veya uygulama izni bulunmayan tıbbi işlem ve tedavi yöntemlerine
    uluslararası sağlık turizmi tanıtım ve bilgilendirme faaliyetlerinde yer
    verilemez.<o:p></o:p></span></p>
                                        <p class="Metin"><span>(3) Birinci fıkranın (a) ve (g) bentleri
    kapsamında, uluslararası sağlık turizmi hizmeti sunan sağlık tesisi ve
    aracı kuruluş tarafından indirim, kampanya ve rekabetçi fiyat duyuruları
    yapılabilir.<o:p></o:p></span></p>
                                        <p class="Metin"><span>(4) Bu madde kapsamı dışında kalan hususlarda bu
    Yönetmeliğin ilgili diğer hükümleri uluslararası sağlık turizmi hizmeti
    sunan sağlık tesisleri ve aracı kuruluşlar için de uygulanır.<o:p></o:p></span></p>
