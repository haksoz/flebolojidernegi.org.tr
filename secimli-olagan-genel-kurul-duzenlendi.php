                                    <p><img class="aligncenter size-full wp-image-519" src="../wp-content/uploads/2020/01/GENEL-KURUL.png" alt="" width="1080" height="315" srcset="../wp-content/uploads/2020/01/GENEL-KURUL.png 1080w, ../wp-content/uploads/2020/01/GENEL-KURUL-300x88.png 300w, ../wp-content/uploads/2020/01/GENEL-KURUL-768x224.png 768w, ../wp-content/uploads/2020/01/GENEL-KURUL-1024x299.png 1024w, ../wp-content/uploads/2020/01/GENEL-KURUL-696x203.png 696w, ../wp-content/uploads/2020/01/GENEL-KURUL-1068x312.png 1068w" sizes="(max-width: 1080px) 100vw, 1080px" /></p>
                                    <p>Saygıdeğer Hocalarımız ve Meslektaşlarımız,</p>
                                    <p>Derneğimizin seçimli olağan genel kurul toplantısı 11 Ocak 2020 tarihinde
                                        Radisson Blue Otel, Şişli’de düzenlenmiştir. Yapılan seçim sonucunda Fleboloji
                                        Derneği yeni dönem yönetim kurulunu seçmekten mutluluk duyuyoruz.</p>
                                    <p>Yeni yönetim kurulumuzun görev dağılımı aşağıdaki gibi belirlenmiştir:</p>
                                    <p>Yönetim Kurulu Başkanı: Serdar Bayrak</p>
                                    <ul>
                                        <li>Başkan Yardımcısı: Erdal Aslım</li>
                                        <li>Genel Sekreter: Aybanu Gökçen</li>
                                        <li>Sayman: Ali Aycan Kavala</li>
                                        <li>Üyeler: Adnan Taner Kurdal, Alptekin Yasım, Suat Doğancı</li>
                                    </ul>
                                    <p>Genel kurulumuza ve seçime teşrifleriyle derneğimizin faaliyetlerini ve
                                        gelişimini destekleyen ve bu vesile ile yeni yönetim kurulunu seçerek bizi bu
                                        onurlu göreve layık gören tüm üyelerimize, eski başkanlarımıza ve değerli
                                        hocalarımıza teşekkürlerimizi ve şükranlarımızı sunmaktayız.</p>
                                    <p>Türk Kalp ve Damar Cerrahisi Derneği ile Ulusal Vasküler ve Endovasküler Cerrahi
                                        Derneği’ne, gerek tüm yönetim süreçlerimizde bizlerle birliktelikleri,
                                        yardımlaşmaları ve destekleri nedeniyle gerekse de genel kurulumuza yönetim
                                        kurulları olarak teşrifleri nedeniyle minnetlerimizi sunarız.</p>
                                    <p>Genel kurulumuzu başarıyla yöneten Murat Sargın, Sadık Kıvanç Metin ve Emrah Oğuz
                                        hocalarımıza yönetim kurulumuz adına teşekkür ederiz.</p>
                                    <p>Yıl boyunca ülkemizin dört bir köşesinde düzenlenen bölgesel toplantılar ile
                                        başlattığımız atılımı 2021 senesinde bir dünya kongresi ile taçlandıracak
                                        olmanın haklı gururunu yaşamaktayız. Ülkemiz açısından son derece önemli bir
                                        etkinlik düzenleyecek olmak hepimizi heyecanlandırmakla birlikte, hep birlikte,
                                        omuz omuza çalışarak bize yakışır bir organizasyonu düzenleyeceğimizden şüphemiz
                                        yoktur.</p>
                                    <p>Yeni yönetim kurulu üyelerimiz ile gerek ulusal düzeydeki toplantılarımızın
                                        hazırlıklarını gerekse de UIP 2021 kongremizin çalışmalarını durmaksızın
                                        yapacağız. Siz değerli üyelerimizin mesleki yaşantılarına katkı sunmak ve her
                                        durumda yanınızda olmak en büyük hedefimizdir. Tüm üyelerimizle birlikte bol
                                        katılımlı toplantılarla önyargıları, hataları, yeni fikirleri ve belirsiz
                                        konuları tartışmak eşsiz bir sinerji yakalamamızı sağlamakta ve geleceğe
                                        sizlerle birlikte umutla bakmamıza vesile olmaktadır.</p>
                                    <p>Fleboloji Derneği, büyük hedeflerine kurumsal kimliğini güçlendirerek ve bugün
                                        başardığı tüm işleri gelecek kuşaklara zengin bir miras olacak aktaracaktır.</p>
                                    <p>Yönetim kurulumuz adına,</p>
                                    <p>Saygılarımla,</p>
                                    <p>Doç. Dr. Serdar BAYRAK<br />
                                        Fleboloji Derneği Yönetim Kurulu Başkanı</p>
