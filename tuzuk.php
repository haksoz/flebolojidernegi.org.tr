                                    sağlanamaması sebebiyle toplantının ertelenmesi durumunda ikinci toplantıda çoğunluk
                                    aranmaz. Ancak, bu toplantıya katılan üye sayısı, yönetim ve denetim kurulları üye
                                    tam sayısının iki katından daha az olamaz.</p>
                                <p><em><strong>       Toplantı Usulü</strong></em><br />
                                    Genel kurula katılma hakkı bulanan üyelerin listesi toplantı yerinde hazır
                                    bulundurulur. Toplantı yerine girecek üyelerin resmi verilmiş kimlik belgeleri,
                                    yönetim üyeleri kurulunca görevlendirilecek görevliler tarafından kontrol edilir.
                                    Üyeler, yönetim kurulunca düzenlenen listedeki adları karşısına imza koyarak
                                    toplantı yerine girerler.<br />
                                    Toplantı yeterli sayısı sağlanmışsa durum bir tutanakla tespit edilir ve toplantı
                                    yönetim kurulum başkanı veya görevlendireceği yönetim kurulu üyelerinden biri
                                    tarafından açılır. Toplantı yeter sayısı sağlanamaması halinde de yönetim kurulunca
                                    bir tutanak düzenlenir.<br />
                                    Açılıştan sonra, toplantıyı yönetmek üzere bir başkan ve yeteri kadar baksan vekili
                                    ile yazman seçilerek divan heyeti oluşturulur.<br />
                                    Dernek organlarının seçimi için yapılacak oylamalarda, oy kullanan üyelerin divan
                                    heyetine kimliklerini göstermeleri ve hazırın listesindeki isimlerin karşılarına
                                    imzalamaları zorunludur.<br />
                                    Toplantının yönetimi ve güvenliğinin sağlanması divan başkanına aittir. Genel kurul,
                                    gündemdeki konuların görüşülerek kara bağlanmasıyla sonuçlandırılır. Genel kurulda
                                    her üyenin bir oy hakkı vardır; üye oyunu kullanmak zorundadır.<br />
                                    Toplantıda görüşülen konular ve alınan karalar bir tutanağa yazılır ve divan başkanı
                                    ile yazmanlar tarafından birlikte imzalanır. Toplantı sonunda, tutanak ve diğer
                                    belgeler yönetim kurulum başkanlığa teslim edilir Yönetim kurulu başkanı bu
                                    belgelerin korunmasından ve yeni seçilen yönetim kuruluna 7 gün içinde teslim etmek
                                    sorumludur.</p>
