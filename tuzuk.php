                                <p><em><strong>         Çağrı Usulü</strong></em><br />
                                    Yönetim kurulu, dernek tüzüğüne göre genel kurula katılma hakkı bulunan üyelerin
                                    listesini düzenler. Genel kurula katılma hakkı bulunan üyeler, en az on beş gün
                                    önceden günü, saati yeri ve gündemi bir gazete ilan edilmek veya yazılı ya da
                                    elektronik posta ile bildirilmek suretiyle toplantıya çağırılır. Bu çağrıda,
                                    çoğunluk sağlanamaması sebebiyle toplantı yapılamazsa, ikinci toplantının hangi gün,
                                    saat ve nerede yapılacağı belirlenir. İlk toplantı ile ikinci toplantı arasında süre
                                    yedi günden az, altmış günden fazla olamaz.<br />
                                    Toplantıda çoğunluk sağlanamaması sebebinin dışında başka bir nedenle geri
                                    bırakılırsa, bu durum geri bırakma sebepleri de belirtilmek suretiyle, ilk toplantı
                                    için yapılan çağrı usulüne uygun olarak üyelere duyurulur. İkinci toplantının geri
                                    bırakma tarihinden itibaren en geç altı ay içinde yapılması zorunludur. Üyeler
                                    ikinci toplantıya, birinci fıkrada belirtilen esaslara göre yeniden çağırılır.<br />
                                    Genel kurul toplantısı bir defadan fazla geri bırakılamaz.</p>
