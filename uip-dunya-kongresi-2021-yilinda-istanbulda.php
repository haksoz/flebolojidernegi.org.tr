                                    <p><img class="aligncenter size-full wp-image-454" src="../wp-content/uploads/2018/02/fleboloji.jpg" alt="" width="800" height="501" srcset="../wp-content/uploads/2018/02/fleboloji.jpg 800w, ../wp-content/uploads/2018/02/fleboloji-300x188.jpg 300w, ../wp-content/uploads/2018/02/fleboloji-768x481.jpg 768w, ../wp-content/uploads/2018/02/fleboloji-696x436.jpg 696w, ../wp-content/uploads/2018/02/fleboloji-671x420.jpg 671w" sizes="(max-width: 800px) 100vw, 800px" /></p>
                                    <p>Sayın Üyelerimiz,</p>
                                    <p>Avustralya’nın Melbourne şehrinde bugün toplanan International Union of
                                        Phlebology (UIP) Genel Kurulu, dört yılda bir düzenlenen ve bir sonraki 2021
                                        yılında gerçekleştirilecek olan UIP Dünya Kongresi’nin Türkiye’de yapılmasına
                                        karar vermiştir. Dünyanın en prestijli fleboloji toplantısı olan bu kongre,
                                        Ulusal Vasküler ve Endovasküler Cerrahi Kongresi ve Fleboloji Kongresi ile
                                        birlikte yapılacaktır.</p>
                                    <p>Uzun yıllar süren yoğun çabalarından dolayı başta Dr. A. Kürşat Bozkurt’a olmak
                                        üzere, Dr. Tankut Akay’a, Dr. Serdar Bayrak’a, Dr. Mustafa Şırlak’a ve Dr. Fatih
                                        İslamoğlu’na teşekkür ederiz. Melbourne Başkonsolosluğumuz tarafından genel
                                        kurul üyeleri için düzenlenen toplantıya büyük destek veren Dışişleri Bakanı
                                        Sayın Mevlüt Çavuşoğlu’na, Melbourne Başkonsolosumuz Sayın Mehmet
                                        Küçüksakallı’ya, bakanımız ile iletişimimizi sağlayan Sayın Dr. Rüçhan Akar’a
                                        şükranlarımızı sunarız.</p>
                                    <p>Melbourne Dünya Kongresi ödül töreni ve kapanış seremonisinde ülkemizi olağanüstü
                                        performansı ile gururlandıran soprano Dr. Karin Bozkurt’a, Melbourne’e gelerek
                                        tüm lojistik destekleri sağlayan Solo Event kongre organizasyon firmasından
                                        Görkem Türköz ile Dilek Burkaya’ya teşekkür ederiz.</p>
                                    <p>Hep birlikte mükemmel bir toplantı yapmak dilekleriyle.</p>
                                    <p>Ulusal Vasküler ve Endovasküler Cerrahi Derneği<br />
                                        Fleboloji Derneği</p>
